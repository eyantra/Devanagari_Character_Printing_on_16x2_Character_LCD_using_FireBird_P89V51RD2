#include<intrins.h>
#include"p89v51rx2.h"
#include"lcd.h"


void main(void)
{
 
 unsigned char pattern[]={0x1f, 0x4, 0xe, 0x15, 0x15, 0xc, 0x4, 0x0,	//Ka
 						  0x1f, 0x9, 0xb, 0x15, 0x13, 0x9, 0x9, 0x0,	//Kha
						  0x1f, 0x9, 0x9, 0x9, 0x9, 0x19, 0x19, 0x0,	//Ga
						  0x1f, 0x11, 0x11, 0xd, 0x11, 0x11, 0xf, 0x1,	//Gha
						  0x1f, 0x2, 0xe, 0x10, 0xe, 0x1, 0x11, 0xe,	//Da
						  0x1f, 0x1, 0x1, 0x1d, 0x9, 0x9, 0x7, 0x1,		//Cha
						  0x1f, 0x2, 0x1f, 0x2, 0xa, 0x4, 0x2, 0x1,		//Rupee Symbol
						  0x00, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0	};	// Blank

 
 unsigned char i=0;
 LCD_INIT();
 LCD_CMD(DON_COFF);

 LCD_CMD(0x40);
 for(i=0;i<64;i++)
 {
  LCD_CHAR(pattern[i]);
 }

 LCD_CMD(LINE1);
 LCD_WRITE("Devnagari ");
 LCD_CHAR(0x00);
 LCD_CHAR(0x01);
 LCD_CHAR(0x02);
 LCD_CHAR(0x03);
 LCD_CHAR(0x04);
 LCD_CHAR(0x05);

 LCD_CMD(LINE2);
 LCD_WRITE("Rupee symbol ");
 LCD_CHAR(0x06);

 while(1);
}